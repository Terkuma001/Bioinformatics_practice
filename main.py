dna_sequence = input("Enter DNA sequence: ")

num_a = dna_sequence.count("A")
num_c = dna_sequence.count("C")
num_g = dna_sequence.count("G")
num_t = dna_sequence.count("T")
num_u = dna_sequence.count("U")

print("Number of A's:", num_a)
print("Number of C's:", num_c)
print("Number of G's:", num_g)
print("Number of T's:", num_t)
print("Number of U's:", num_u)